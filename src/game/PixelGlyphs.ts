export function toPixelGlyph(value: string): PixelGlyph {
  const normalized = value === "—" ? "-" : value.toUpperCase()

  if (isPixelGlyph(normalized)) {
    return normalized
  }

  return " "
}

export function isPixelGlyph(value: string): value is PixelGlyph {
  return PIXEL_GLYPHS.some((glyph) => glyph === value)
}

export function getPixelGlyphPatternWidth(glyph: PixelGlyph): number {
  return PixelGlyphPatterns[glyph].reduce((width, row) => Math.max(width, row.length), 0)
}
